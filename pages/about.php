                <p class="fw-medium"><b class="text-primary">1. Infrastruktur Jaringan:</b> <br> Menyediakan jaringan internet dan intranet yang mendukung aktivitas akademik dan administrasi di kampus.</p>
                <p class="fw-medium"><b class="text-primary">2. Sistem Informasi Akademik (SIAKAD):</b> <br> Mengelola sistem administrasi akademik seperti pendaftaran, pengisian nilai, dan pengaturan jadwal perkuliahan.</p>
                <p class="fw-medium"><b class="text-primary">3. Manajemen Data:</b> <br> Menyimpan dan mengelola data penting, termasuk data mahasiswa, dosen, dan data administratif lainnya.</p>
                <p class="fw-medium"><b class="text-primary">4. Keamanan IT:</b> <br> Memastikan keamanan informasi serta infrastruktur teknologi digital kampus.</p>
                <p class="fw-medium"><b class="text-primary">5. Layanan Teknis:</b> <br> Memberikan dukungan teknis seperti instalasi perangkat lunak, pemeliharaan perangkat keras, serta bantuan kepada pengguna.</p>
                <p class="fw-medium"><b class="text-primary">6. Pengembangan Aplikasi:</b> <br> Membangun dan mengelola aplikasi yang mendukung kegiatan akademik serta administrasi kampus.</p>
                <p class="fw-medium"><b class="text-primary">7. Learning Management System (LMS):</b> <br> Mendukung pembelajaran daring dengan menyediakan platform e-learning, memungkinkan interaksi antara dosen dan mahasiswa, serta pengelolaan tugas dan ujian secara online.</p>
                <p class="fw-medium"><b class="text-primary">8. Pengembangan Website Kampus:</b> <br> Mengelola dan memperbarui website resmi kampus untuk memberikan informasi terkini tentang program, kegiatan, dan pengumuman.</p>
                <p class="fw-medium"><b class="text-primary">9. Manajemen Akses Digital:</b> <br> Mengelola akun pengguna untuk layanan digital kampus, seperti email, sistem akademik, dan layanan lainnya.</p>
                <p class="fw-medium"><b class="text-primary">10. Kolaborasi Digital:</b> <br> Memfasilitasi kerja sama antar fakultas dan unit kerja dalam hal penggunaan teknologi untuk meningkatkan efektivitas dan efisiensi kerja.</p>
                <p class="fw-medium"><b class="text-primary">11. Inovasi Teknologi:</b> <br> Merancang dan menerapkan kebijakan terkait teknologi baru seperti cloud computing, virtualisasi, serta pemanfaatan big data untuk mendukung pengelolaan informasi kampus.</p>
