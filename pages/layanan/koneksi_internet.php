                <p class="fw-medium"><b class="text-primary">1. Jaringan Kampus (Wi-Fi & LAN)</b> </p>
                <p>menyediakan jaringan internet nirkabel (Wi-Fi) yang mencakup seluruh area kampus, termasuk ruang kelas, perpustakaan, dan area umum. Selain itu, jaringan kabel (LAN) tersedia di area khusus seperti laboratorium dan ruang administrasi.</p>
                <p class="fw-medium"><b class="text-primary">2. Kecepatan & Kapasitas</b> </p>
                <p>Layanan koneksi internet dirancang dengan kapasitas bandwidth yang memadai untuk mendukung kebutuhan mahasiswa dan staf dalam mengakses bahan ajar, riset, komunikasi, serta aktivitas daring lainnya.</p>
                <p class="fw-medium"><b class="text-primary">3. Aksesibilitas</b> </p>
                <p>Setiap pengguna, baik mahasiswa, dosen, maupun tenaga kependidikan, mendapatkan akun resmi yang terintegrasi dengan sistem login ke jaringan internet kampus. Ini memungkinkan akses yang mudah dan aman ke jaringan Wi-Fi di seluruh lingkungan kampus.</p>
                <p class="fw-medium"><b class="text-primary">4. Keamanan Jaringan</b> </p>
                <p>Pusat Teknologi Informasi Uniga memastikan keamanan koneksi internet dengan penerapan protokol keamanan seperti enkripsi jaringan dan firewall. Ini untuk melindungi data pengguna serta menghindari akses yang tidak sah atau aktivitas siber berbahaya.</p>
                <p class="fw-medium"><b class="text-primary">5. Pemantauan & Pemeliharaan</b> </p>
                <p>Jaringan internet dipantau secara berkala untuk menjaga performa dan kestabilan koneksi. Tim teknis juga secara rutin melakukan pemeliharaan infrastruktur untuk mencegah gangguan dan meningkatkan kualitas layanan.</p>
                <p class="fw-medium"><b class="text-primary">6. Dukungan Teknis</b> </p>
                <p>Uniga menyediakan layanan bantuan teknis bagi pengguna yang mengalami kesulitan dalam mengakses atau menggunakan jaringan internet kampus, baik secara langsung maupun melalui sistem tiket online.</p>

